local HEIGHT = 8

local function big_text(str)
    str = str:upper()

    local lines = {}
    for i = 1, HEIGHT do
        lines[i] = ""
    end

    for char in str:gmatch(".") do
        local letter = font[char] or font['?'] or {
            " ????     ",
            " ?   ?    ",
            " ?   ?    ",
            "  ???     ",
            "     ?    ",
            "     ?    ",
            "     ?    ",
            " ??????   "
        }

        for i = 1, HEIGHT do
            lines[i] = lines[i] .. letter[i] .. " "
        end
    end

    -- выводим и убираем лишние пробелы справа
    for i = 1, HEIGHT do
        print((lines[i]:gsub("%s+$", "")))
    end
end

-- Тест / использование
if arg and arg[1] then
    big_text(table.concat(arg, " "))
else
    print("Введи слово (английские буквы и цифры):")
    local input = io.read("*line")
    if input and input:match("%S") then
        big_text(input)
    end
end